int	ft_is_prime(int nb)
{
	int	cnt;

	cnt = 2;
	if (nb <= 1)
		return (0);
	while (cnt < nb)
	{
		if (nb % cnt == 0)
			return (0);
		cnt++;
	}
	return (1);
}
