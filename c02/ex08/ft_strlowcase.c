char	*ft_strlowcase(char *str)
{
	int	idx;

	idx = 0;
	while (str[idx] != '\0')
	{
		if ('A' <= str[idx] && str[idx] <= 'Z')
		{
			str[idx] += 'a' - 'A';
		}
		idx++;
	}
	return (str);
}
